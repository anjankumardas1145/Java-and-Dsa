package PAtternn_2;

public class nine_one {
    public static void main(String[] args) {
        int num=9;
        int n=3;
        for (int i=0;i<n;i++){
            for (int j=0;j<n;j++){
                System.out.print(num+ " ");
                num--;

            }
            System.out.println();
        }
    }
}
