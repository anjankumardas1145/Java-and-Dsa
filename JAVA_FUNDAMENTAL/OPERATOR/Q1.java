package JAVA_FUNDAMENTAL.OPERATOR;

public class Q1 {
    public static void main(String[] args) {
        //swap of two numbers
        int a=9;
        int b=6;
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping the a will be:"+a);
        System.out.println("After swapping the value of b will be:"+b);
    }
}
