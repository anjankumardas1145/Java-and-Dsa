package LOOP;

public class print_one_to_ten {


    //Defination of loop:Code that runs multiple time based on a condition.
    //repeated execution of code.
    //loop operates repetive task.
    //number of times the loop iterates is call iteration
    public static void main(String[] args) {
        int n=1;
        while(n<=10){
            System.out.print(n+" ");
            n++;
        }
    }
}
